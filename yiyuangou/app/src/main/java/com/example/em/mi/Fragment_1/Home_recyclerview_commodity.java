public class Home_recyclerview_commodity {
    private int pic;
    //开始时间
    private String start_time;
    //系统时间
    private String now_time;
    //算出活动结束时间和当前系统时间的时间差，用ms为单位
    private long countTime;

    public Home_recyclerview_commodity(String now_time, int pic, String start_time, long countTime, int mType, String time) {
        this.now_time = now_time;
        this.pic = pic;
        this.start_time = start_time;
        this.countTime = countTime;
        this.mType = mType;
        this.time = time;
    }

    public Home_recyclerview_commodity() {
    }

    //存储每个商品的倒计时的具体时间格式，并最后用TextView显示，
    // 如"42天2时58分20秒"
    private String time;
    private int mType;

    public int getPic() {
        return pic;
    }

    public String getStart_time() {
        return start_time;
    }

    public String getNow_time() {
        return now_time;
    }

    public long getCountTime() {
        return countTime;
    }

    public String getTime() {
        return time;
    }

    public int getmType() {
        return mType;
    }

    public void setPic(int pic) {
        this.pic = pic;
    }

    public void setStart_time(String start_time) {
        this.start_time = start_time;
    }

    public void setCountTime(long countTime) {
        this.countTime = countTime;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public void setmType(int mType) {
        this.mType = mType;
    }

    public void setNow_time(String now_time) {
        this.now_time = now_time;
    }


}
